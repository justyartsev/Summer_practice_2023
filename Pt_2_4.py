print("ax^2+bx+c=0")
a = int(input("Введите коэффициент а: "))
b = int(input("Введите коэффициент b: "))
c = int(input("Ввежите коэффициент с: "))
d = b ** 2 - 4 * a * c
if d > 0:
    x1 = (-b + d ** 0.5) / (2 * a)
    x2 = (-b - d ** 0.5) / (2 * a)
    print(f"Корни квадратного уравнения: {x1} и {x2}")
elif d == 0:
    x = (-b) / (2 * a)
    print(f"Корень квадратного уравнения: {x}")
else:
    print("уравнение не имеет корней")
